#!/usr/bin/env bash
# =============================================================================
# Claude Code スラッシュコマンド デモ
# =============================================================================
# このスクリプトは Claude Code の主要コマンドを非インタラクティブに示します。
# 実際の体験は `claude` を起動してインタラクティブモードで試してください。
# =============================================================================

set -euo pipefail

BOLD='\033[1m'
CYAN='\033[0;36m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
RESET='\033[0m'

header() { echo -e "\n${BOLD}${CYAN}== $* ==${RESET}"; }
info()   { echo -e "${GREEN}▶${RESET} $*"; }
note()   { echo -e "${YELLOW}NOTE:${RESET} $*"; }
cmd()    { echo -e "  ${BOLD}\$${RESET} $*"; }

# ------------------------------------------------------------------------------
header "Claude Code スラッシュコマンド デモ"
# ------------------------------------------------------------------------------
echo ""
echo "このデモでは Claude Code の代表的なスラッシュコマンドを紹介します。"
echo "実際に試すには 'claude' コマンドでインタラクティブモードを起動してください。"

# ------------------------------------------------------------------------------
header "1. セッション管理コマンド"
# ------------------------------------------------------------------------------
info "コンテキスト / コスト確認"
cmd "/context     # トークン使用量と残量を確認"
cmd "/cost        # APIコスト（累計）を確認"
cmd "/compact     # 手動でコンテキストを圧縮（トークン節約）"
cmd "/clear       # 会話をリセット（コンテキスト破棄）"
echo ""

info "セッション管理"
cmd "/session     # 現在のセッションID確認・過去セッション一覧"
echo ""
note "/clear は SessionEnd フックを 'clear' マッチャーでトリガーします"

# ------------------------------------------------------------------------------
header "2. 情報・診断コマンド"
# ------------------------------------------------------------------------------
info "ヘルプと診断"
cmd "/help        # 全コマンドとキーボードショートカット一覧"
cmd "/doctor      # インストール・設定・フックの診断"
cmd "/debug       # デバッグモード切替（APIレスポンス等を表示）"
echo ""
note "困ったときはまず /doctor を実行してください"

# ------------------------------------------------------------------------------
header "3. 設定・カスタマイズコマンド"
# ------------------------------------------------------------------------------
info "設定管理"
cmd "/config      # settings.json をインタラクティブに編集"
cmd "/keybindings # ~/.claude/keybindings.json をエディタで開く"
cmd "/memory      # CLAUDE.md（メモリ）を表示・編集"
cmd "/permission  # 現在の権限ルールを一覧表示"
cmd "/statusline  # ステータスラインの表示設定"
cmd "/vim         # Vim モードのオン/オフ"
echo ""

# ------------------------------------------------------------------------------
header "4. 拡張機能コマンド"
# ------------------------------------------------------------------------------
info "フック・MCP・エージェント管理"
cmd "/hooks       # フックのCRUD操作"
cmd "/mcp         # MCPサーバーの管理（追加・削除・認証）"
cmd "/agents      # サブエージェントの管理"
cmd "/skills      # スキルの一覧と管理"
cmd "/plugin      # プラグインの探索・有効化"
echo ""

# ------------------------------------------------------------------------------
header "5. タスク支援コマンド"
# ------------------------------------------------------------------------------
info "コミット作成"
cmd "/commit                           # AIがメッセージを生成"
cmd "/commit \"feat: add login page\"    # メッセージを指定"
echo ""

info "PR レビュー（GitHub CLI が必要）"
cmd "/review-pr 123"
cmd "/review-pr https://github.com/owner/repo/pull/123"
echo ""
note "PR レビューには gh auth login が必要です"

# ------------------------------------------------------------------------------
header "6. MCP プロンプトコマンド"
# ------------------------------------------------------------------------------
info "MCPサーバーが提供するプロンプトは /mcp__<server>__<prompt> で呼び出せます"
cmd "/mcp__github__list_prs"
cmd "/mcp__github__pr_review 456"
cmd "/mcp__jira__create_issue \"バグ修正\" high"
echo ""
note "MCP サーバーの設定は 04-mcp-tool-extension/ を参照"

# ------------------------------------------------------------------------------
header "実際に試してみよう"
# ------------------------------------------------------------------------------
echo ""
echo "  1. 'claude' コマンドで起動"
echo "  2. 何か質問して会話を始める"
echo "  3. /context でトークン使用量を確認"
echo "  4. /cost でAPIコストを確認"
echo "  5. /commit でコミットを作成"
echo "  6. /help で全コマンドを確認"
echo ""
